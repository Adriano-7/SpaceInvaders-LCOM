/**
 * @file direction.h
 * @brief Definition of direction enumerations.
 */

#pragma once

#include <lcom/lcf.h>

/**
 * @enum DirectionX
 * @brief Enumeration for horizontal directions.
 */
enum DirectionX {
    LEFT,   /**< Left direction. */
    RIGHT   /**< Right direction. */
};

/**
 * @enum DirectionY
 * @brief Enumeration for vertical directions.
 */
enum DirectionY {
    UP,     /**< Up direction. */
    DOWN    /**< Down direction. */
};
